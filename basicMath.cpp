#include <iostream>
#include <cmath>

int main() {
    std::cout << "3+3=" << (3+3) << std::endl;
    std::cout << "12-3=" << (12-3) << std::endl;
    std::cout << "15x8=" << (15*8) << std::endl;
    std::cout << "11/4=" << (11/4) << std::endl;
    std::cout << "2^7=" << (2^7) << std::endl;
    std::cout << "square root of 80 =" << sqrt(80) << std::endl;
}
